    <h1>Amazon surprises Wall Street with a blowout holiday quarter</h1>
    <p>Case in point: The e-commerce heavyweight's profits crashed in the second and third quarters of 2019, following a heady string of record earnings. And just when profits seemed to be on their way down, they're right back up again in the fourth quarter, just shy of the company's all-time quarterly record.

        Amazon's stock jumped on the better-than-expected earnings and sales for the all-important holiday quarter, which Amazon reported Thursday afternoon. Shares surged 11% in after-hours trading.
        
        To add to the good news, the company revealed it now has over 150 million paid Prime members globally, up from over 100 million, which it reported in April 2018.
        
        Amazon's latest quarterly results come as Wall Street continues to be heavily focused on the company's revenue growth as a primary metric to judge its success. While that metric has been a source of strength for Amazon for years, it's become harder for the company to maintain hefty percentage gains since it's now so big, even as it's pushed into new fields like health care, robotics, groceries and film production.</p>
